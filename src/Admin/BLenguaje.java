package Admin;

public class BLenguaje {
	String nombre;
	String valor;
	String icono;
	
	public String getNombre() {
		return nombre;
	}
	public String getValor() {
		return valor;
	}
	public String getIcono() {
		return icono;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public void setValor(String valor) {
		this.valor = valor;
	}
	public void setIcono(String icono) {
		this.icono = icono;
	}
}
